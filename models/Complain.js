const mongoose = require('mongoose');
const complaintSchema = new mongoose.Schema({
    firstName: {
        type: String,
        required: true
    },
    lastName: {
        type: String,
        required: true
    },
    email: {
        type: String,
        required: true
    },
    address: {
        type: String,
        required: true
    },
    city: {
        type: String,
        required: true
    },
    state: {
        type: String,
        required: true
    },
    phone: {
        type: String,
        required: true
    },
    submittedAt: {
        type: Date,
        default: Date.now
    },
    status: {
        type: String,
        default: 'Pending', // 'Pending', 'Resolved'
    },
});
const Complaint = mongoose.model('Complain', complaintSchema);
module.exports = Complaint;
